package com.arwantech.docjavaui.domain;

/**
 *
 * @author AriMaulana99
 */
public class Person {
    
    // PROPERTY atau FIELD
    private int id;
    private String name;
    private int age;
    private String address;

    public Person() {
        
    }

    public Person(int id, String name, int age, String address) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.address = address;
    }
    
    // METHOD SETTER DAN GETTER UNTUK PENGAKSESAN PROPERTY DI KELAS LAIN
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
    
    
}
